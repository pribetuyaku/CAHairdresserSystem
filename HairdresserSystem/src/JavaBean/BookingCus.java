
package JavaBean;

/**
 *
 * @author Betuyaku
 */
public class BookingCus {
    private int idBookingCust;
    private int idCus;
    private String fname;
    private String Date;
    private String Time;

    public int getIdBookingCust() {
        return idBookingCust;
    }

    public void setIdBookingCust(int idBookingCust) {
        this.idBookingCust = idBookingCust;
    }

    public int getidCus() {
        return idCus;
    }

    public void setidCus(int idCus) {
        this.idCus = idCus;
    }

    public String getfname() {
        return fname;
    }

    public void setfname(String fname) {
        this.fname = fname;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String Date) {
        this.Date = Date;
    }

    public String getTime() {
        return Time;
    }

    public void setTime(String Time) {
        this.Time = Time;
    }
  
    
}
